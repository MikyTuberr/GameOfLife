
# CLASS WHICH SPECIFIES FIELD ON THE GRID
class Point:
    def __init__(self, newX, newY):
        self.x = newX
        self.y = newY

    # GETTERS
    def getX(self):
        return self.x

    def getY(self):
        return self.y

    # SETTERS
    def setX(self, x):
        self.x = x

    def setY(self, y):
        self.y = y

